package com.example.firstproject

fun main(args: Array<String>) {

    var number:Double = 8.5
    var isCheck:Boolean = true

    println("+number = " + +number)
    println("-number = " + -number)
    println("++number = " + ++number)
    println("--number = " + --number)
    println("!isCheck = " + !isCheck)

    println("-------------------------")

    var result:Double = 1.8

    println("result++ = " + result++)
    println("result = " + result)
    println("++result = " + ++result)
    println("result = " + result)
}